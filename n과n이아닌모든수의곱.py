# 9 개의 요소를 가진 리스트 ls
ls = [1, 2, 3, 4, 5, 6, 7, 8, 9]
# 1 * 2, 1 * 3, ... , 1 * 9, 2 * 3, ... , 8 * 9

ls2 = [] # 각 요소를 다 한 번씩 서로 곱한 값이 들어갈 리스트 ls2
for i in range(len(ls)-1): # 0 1 2 3 4 5 6 7 8
    for j in range(i+1, len(ls)): # i 0| 1, 2, 3, 4, 5, 6, 7, 8
        ls2.append(ls[i]*ls[j])

print(ls2) # [2, 3, 4, 5, 6, 7, 8, 9, 6, 8, 10, 12, 14, 16, 18, 12, 15, 18, 21, 24, 27, 20, 24, 28, 32, 36, 30, 35, 40, 45, 42, 48, 54, 56, 63, 72]
